class LineRender {
    unsigned int VBO, VAO;
    int num;
    //	float* vertexs;

public:
    LineRender(Pnts points) {
        num = points.size();

        //-----------------------------
        std::vector<float> vertexs(points.size() * 3);
        //vertexs = new float[points.size() * 3];
        for (int i = 0; i < points.size(); i++) {
            vertexs[i * 3 + 0] = points[i][0];
            vertexs[i * 3 + 1] = points[i][1];
            vertexs[i * 3 + 2] = points[i][2];
        }


        //---------------------------
        glGenVertexArrays(1, &this->VAO);
        glGenBuffers(1, &this->VBO);
        glBindVertexArray(this->VAO);
        glBindBuffer(GL_ARRAY_BUFFER, this->VBO);
        glBufferData(GL_ARRAY_BUFFER, num * 3 * sizeof(float), vertexs.data(), GL_STATIC_DRAW);
        glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
        glEnableVertexAttribArray(0);
        glBindBuffer(GL_ARRAY_BUFFER, 0);
        glBindVertexArray(0);
    }

    ~LineRender() {
        glDeleteVertexArrays(1, &this->VAO);
        glDeleteBuffers(1, &this->VBO);
        //	delete[] vertexs;
    }

    void Draw() {
        glBindVertexArray(this->VAO);
        glDrawArrays(GL_LINE_STRIP, 0, num);
        glBindVertexArray(0);
    }
};

class PolyCurve {
    std::vector<float> coffs;

public:
    PolyCurve(std::vector<float> coefficients) {
        coffs = coefficients;
    }
    float getPoint(float t) {
        float res = 0.0f;
        for (int i = 0; i < coffs.size(); i++) {
            res += coffs[i] * pow(t, i);
        }
        return res;
    }

    Pnts Sample(int m) {
        Pnts points;
        for (size_t i = 0; i < m; i++) {
            float t = i / (m - 1.0f);
            float y = getPoint(t);
            float z = getPoint(t / 2);

            points.push_back({ t,y,0.0 });
        }
        return points;
    }
};

